def story_dialog(index: int, planet_name: str) -> list[str]:
    """Return the story fragment for a planet contact.

    Args:
        index: Zero-based story progression index.
        planet_name: Name of the contacted planet.

    Returns:
        Dialog lines with a planet-specific heading.
    """

    fragment = STORY_DIALOGS[index % len(STORY_DIALOGS)]
    return [f"{planet_name}: архив покаяния {index % len(STORY_DIALOGS) + 1}/20.", *fragment]